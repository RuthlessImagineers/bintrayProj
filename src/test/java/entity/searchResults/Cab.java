public class Cab {

    private String cabProviderTitle;
    private String cabProviderPrice;
    private String cabProviderBase;
    private String cabProviderEmission;
    private String cabProviderPaymentType;

    public String getCabProviderTitle() {
        return cabProviderTitle;
    }

    public void setCabProviderTitle(String cabProviderTitle) {
        this.cabProviderTitle = cabProviderTitle;
    }

    public String getCabProviderPrice() {
        return cabProviderPrice;
    }

    public void setCabProviderPrice(String cabProviderPrice) {
        this.cabProviderPrice = cabProviderPrice;
    }

    public String getCabProviderBase() {
        return cabProviderBase;
    }

    public void setCabProviderBase(String cabProviderBase) {
        this.cabProviderBase = cabProviderBase;
    }

    public String getCabProviderEmission() {
        return cabProviderEmission;
    }

    public void setCabProviderEmission(String cabProviderEmission) {
        this.cabProviderEmission = cabProviderEmission;
    }

    public String getCabProviderPaymentType() {
        return cabProviderPaymentType;
    }

    public void setCabProviderPaymentType(String cabProviderPaymentType) { this.cabProviderPaymentType = cabProviderPaymentType; }
}
