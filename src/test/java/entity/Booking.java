package entity;

public class Booking {

    private String bookedTitle;
    private String bookedPrice;
    private String bookedFrom;
    private String bookedTo;
    private String bookedTripBase;
    private String bookedEmission;
    private String bookedDate;

    public String getBookedTitle() {
        return bookedTitle;
    }

    public void setBookedTitle(String bookedTitle) {
        this.bookedTitle = bookedTitle;
    }

    public String getBookedPrice() {
        return bookedPrice;
    }

    public void setBookedPrice(String bookedPrice) {
        this.bookedPrice = bookedPrice;
    }

    public String getBookedFrom() {
        return bookedFrom;
    }

    public void setBookedFrom(String bookedFrom) {
        this.bookedFrom = bookedFrom;
    }

    public String getBookedTo() {
        return bookedTo;
    }

    public void setBookedTo(String bookedTo) {
        this.bookedTo = bookedTo;
    }

    public String getBookedTripBase() {
        return bookedTripBase;
    }

    public void setBookedTripBase(String bookedTripBase) {
        this.bookedTripBase = bookedTripBase;
    }

    public String getBookedEmission() {
        return bookedEmission;
    }

    public void setBookedEmission(String bookedEmission) {
        this.bookedEmission = bookedEmission;
    }

    public String getBookedDate() {
        return bookedDate;
    }

    public void setBookedDate(String bookedDate) {
        this.bookedDate = bookedDate;
    }
}
